using UnityEngine;
using System.Collections;

public class DataControl : MonoBehaviour {

    public static DataControl control;

    void Awake()
    {
        if (control == null)
        {
            DontDestroyOnLoad(gameObject);
            control = this;
        }
        else if (control != this)
        {
            Destroy(gameObject);
        }
    }
}
